public class Sum {
    public static void main(String[] args) {
        int a = 10;
        int b = 40;
        int sum;
        sum = a + b;
        System.out.println("The sum is: " + sum);

        int diff = a - b;
        System.out.println("The difference is: " + diff);

        int mul = a * b;
        System.out.println("The Multiplication is: " + mul);

        int div = a / b;
        System.out.println("The Division is: " + div);
    }
}
